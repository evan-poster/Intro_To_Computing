package week2;

public class MidtermUtil {
    long myQ(int num) {
        return num * num;
    }

    int myMod(int a, int b) {
        return a % b;
    }

    int myDiv(int a, int b) {
        return a / b;
    }

    String myMessage(String message) {
        return "Hello " + message;
    }

    static double getE() {
        return Math.E;
    }
}
